
DECLARE @NR_PEDIDO VARCHAR(20) = '5501785967' /*Número do pedido dentro do arquivo*/
DECLARE @NR_PROCESSO VARCHAR(20)
SET @NR_PROCESSO = (SELECT TOP 1 NR_PROCESSO FROM TPROCESSO WHERE NR_REFERENCIA = @NR_PEDIDO)
DECLARE @NR_INSTRUCAO_EMBARQUE VARCHAR(20) 
SET @NR_INSTRUCAO_EMBARQUE = (SELECT TOP 1 ID_CODIGO_INSTR_DESEMB FROM TPO_ITEM WHERE NR_PROCESSO = @NR_PEDIDO AND ID_CODIGO_INSTR_DESEMB IS NOT NULL)
SELECT 'TPROCESSO', NR_REFERENCIA, * FROM TPROCESSO WHERE NR_REFERENCIA = @NR_PEDIDO
SELECT 'TINSTRUCAO_DESEMBARACO', * FROM TINSTRUCAO_DESEMBARACO WHERE ID_CODIGO = @NR_INSTRUCAO_EMBARQUE
SELECT 'TPO', * FROM TPO WHERE NR_PROCESSO = @NR_PEDIDO
SELECT 'TFLP_PO', * FROM TFLP_PO WHERE /*CD_EVENTO  IN ('003','022') AND */ NR_PROCESSO = @NR_PEDIDO AND CD_EMPRESA  = '00500'
SELECT 'TPO_ITEM', ID_CODIGO_INSTR_DESEMB, * FROM TPO_ITEM WHERE NR_PROCESSO = @NR_PEDIDO
SELECT 'TFLP_PO_ITEM', * FROM TFLP_PO_ITEM  WHERE CD_EVENTO  IN ('257', '798', '275', '879', '880', '881', '955')   AND NR_PROCESSO = @NR_PEDIDO AND CD_EMPRESA  = '00500'
SELECT 'TINSTRUCAO_DESEMBARACO_PEDIDO', * FROM TINSTRUCAO_DESEMBARACO_PEDIDO WHERE ID_CODIGO = @NR_INSTRUCAO_EMBARQUE