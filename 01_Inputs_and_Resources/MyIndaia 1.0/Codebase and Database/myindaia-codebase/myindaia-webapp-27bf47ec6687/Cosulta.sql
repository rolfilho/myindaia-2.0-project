TD.NM_TIPO_DOCTO AS [Tipo de Documento],
PD.NM_DESCRICAO AS [Descriçăo],
CONVERT(VARCHAR(10),PD.DT_CRIACAO,103) AS [Data criaçăo],
PD.NM_ARQUIVO AS [Nome do Arquivo]
